import mongoose from "mongoose";
import ReportModel from "../models/report.model";
import UserModel from "../models/user.model";

// CREATE REPORT
export const createReport = async (reportData: any) => {
  try {
    const {
      doctorId,
      patientId,
      xRayImage,
      keyFindings,
      heartSize,
      normalHeartSize,
      risk,
      aiConfidence,
      doctorNotes,
      status,
      title,
      description,
    } = reportData;

    // Validate doctorId & patientId
    if (!doctorId || !patientId) {
      return {
        success: false,
        message: "Doctor ID and Patient ID are required",
      };
    }

    const doctor = await UserModel.findById(doctorId);
    if (!doctor || doctor.role !== "doctor") {
      return { success: false, message: "Invalid doctor ID" };
    }

    const patient = await UserModel.findById(patientId);
    if (!patient || patient.role !== "patient") {
      return { success: false, message: "Invalid patient ID" };
    }

    // Validate X-ray image
    if (!xRayImage) {
      return { success: false, message: "X-ray image is required" };
    }

    // Build the full report object
    const report = new ReportModel({
      doctorId,
      patientId,
      xRayImage,
      keyFindings,
      heartSize,
      normalHeartSize,
      risk,
      aiConfidence,
      doctorNotes: doctorNotes || null,
      status: status || "completed",

      recommendedNextStep: {
        title,
        description,
      },
    });

    await report.save();

    return {
      success: true,
      data: report,
      message: "Report created successfully",
    };
  } catch (error: any) {
    return { success: false, message: error.message };
  }
};

