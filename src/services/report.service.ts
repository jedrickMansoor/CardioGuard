// GET ALL REPORTS GENERATED BY A DOCTOR
export const getReportsGeneratedByDoctor = async (
  doctorId: string,
  filters: any,
) => {
  try {
    if (!doctorId) {
      return { success: false, message: "Doctor ID is required" };
    }

    const query: any = { doctorId };
    let sort: any = { createdAt: -1 };

    // TIME FILTER
    if (filters?.timeDate) {
      if (filters.timeDate === "thisWeek") {
        const oneWeekAgo = new Date();
        oneWeekAgo.setDate(oneWeekAgo.getDate() - 7);
        query.createdAt = { $gte: oneWeekAgo };
      } else if (filters.timeDate === "thisMonth") {
        const oneMonthAgo = new Date();
        oneMonthAgo.setMonth(oneMonthAgo.getMonth() - 1);
        query.createdAt = { $gte: oneMonthAgo };
      } else if (filters.timeDate === "today") {
        const today = new Date();
        today.setHours(0, 0, 0, 0);
        const tomorrow = new Date(today);
        tomorrow.setDate(tomorrow.getDate() + 1);
        query.createdAt = { $gte: today, $lt: tomorrow };
      }
    }

    // RISK FILTER
    if (filters?.riskLevel) {
      query.risk = filters.riskLevel;
    }

    // SORTING
    if (filters?.sortBy) {
      if (filters.sortBy === "timeDate") {
        sort = { createdAt: -1 };
      } else if (filters.sortBy === "name") {
        // sorting by populated field is allowed
        sort = { "patientId.name": 1 };
      }
    }

    // FINAL QUERY
    let reports = await ReportModel.find(query)
      .populate("patientId", "name email picture age gender")
      .sort(sort);

    // PATIENT NAME SEARCH
    if (filters?.search) {
      const regex = new RegExp(filters.search, "i");

      reports = reports.filter((report: any) =>
        regex.test(report?.patientId?.name),
      );
    }

    return {
      success: true,
      data: reports,
      message: "Reports retrieved successfully",
    };
  } catch (error: any) {
    return { success: false, message: error.message };
  }
};

