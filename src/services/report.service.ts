// REPORT COUNT PER DOCTOR
export const getPatientReportsGeneratedByDoctorCount = async (
  patientId: string,
) => {
  try {
    const result = await ReportModel.aggregate([
      {
        $match: {
          patientId: new mongoose.Types.ObjectId(patientId),
        },
      },
      {
        $sort: { createdAt: -1 },
      },
      {
        $group: {
          _id: "$doctorId",
          reportsCount: { $sum: 1 },
          latestReport: { $first: "$$ROOT" },
        },
      },
      {
        $lookup: {
          from: "users",
          localField: "_id",
          foreignField: "_id",
          as: "doctor",
        },
      },
      {
        $unwind: "$doctor",
      },
      {
        $project: {
          _id: 0,
          doctor: {
            _id: "$doctor._id",
            name: "$doctor.name",
            email: "$doctor.email",
            picture: "$doctor.picture",
          },
          reportsCount: 1,
          latestReport: 1,
        },
      },
    ]);

    return {
      success: true,
      message: "Report count retrieved successfully",
      data: result,
    };
  } catch (error: any) {
    return { success: false, message: error.message };
  }
};

